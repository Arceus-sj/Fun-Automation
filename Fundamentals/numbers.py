num1 = 10
num2 = 2

# Addition
print("Addition: " + str(num1+num2))

# Subtraction
print("Subtraction: " + str(num1-num2))

# Multiplication
print("Multiplication: " + str(num1*num2))

# Division
print("Dividion: " + str(num1/num2))

# Modulas
print("Remainder: " + str(num1%num2))

# Power
print("Power: " + str(num1**num2))

# Example
distance1 = 188
distance2 = 247
totalDistance = distance1 + distance2
mph = 65
timeTaken = totalDistance / mph
print("Total time taken to cover total distance",timeTaken)

# Round function
# Syntax: round(variable, round_value)
print("Total time taken to cover total distance " + str(round(timeTaken,2)))
print(round(timeTaken, 3))